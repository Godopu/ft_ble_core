// make to set Intensity
func makeIntensity(params []byte) []byte {
	// header
	var header = []byte{0xFF, 0xFF, 0xFD, 0x00, 0x19, 0x13}
	// checksum
	var checksum = []byte{0x13}
	// make
	var intensity = append(header, params...)
	intensity = append(intensity, checksum...)
	return intensity
}

